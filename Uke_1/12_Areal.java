class Areal {
    public static void main (String[] args) {
        int bredde = 10;
        int lengde = 20;
        int areal;

        System.out.println("Bredde: " + bredde);
        System.out.println("Lengde: " + lengde);

        areal = bredde * lengde;
        System.out.println("Areal: " + areal);
    }
}
